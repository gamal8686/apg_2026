class EmployeeModel {
  final String id;
  final String name;
  final String department;
  final String jobTitle;
  final String phone;
  final String email;
  final String image;
  final String shift;

  EmployeeModel({
    required this.id,
    required this.name,
    required this.department,
    required this.jobTitle,
    required this.phone,
    required this.email,
    required this.image,
    required this.shift,
  });
}